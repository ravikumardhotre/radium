function trim() {

    console.log("   Ravikumar  ".trim());
}

function changeToLowercase() {

    console.log("RavIkuMar".toLowerCase());
}

function changeToUppercase() {

    console.log("RavIkuMar".toUpperCase())
}

module.exports.trimInput = trim
module.exports.changeToLowercaseInput = changeToLowercase
module.exports.changeToUppercaseInput = changeToUppercase